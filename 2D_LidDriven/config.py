"""
二维顶盖驱动方腔流动 PINN 重构项目配置文件。

项目参数集中放在这里，后续调整训练规模、网络结构和输出路径时只需要改本文件。
"""

import os
import numpy as np


# =========================
# 1. 路径设置
# =========================
项目根目录 = os.path.dirname(os.path.abspath(__file__))
OpenFOAM数据目录 = os.path.join(项目根目录, "OF_data")
合并场数据文件 = os.path.join(OpenFOAM数据目录, "merged_time_steps.mat")
边界数据文件 = os.path.join(OpenFOAM数据目录, "all_boundaries.mat")
时间索引文件 = os.path.join(OpenFOAM数据目录, "cavity", "time_step_to_vtk_files.csv")

数据目录 = os.path.join(项目根目录, "数据")
图片目录 = os.path.join(项目根目录, "图片")
表格目录 = os.path.join(项目根目录, "结果表格")
模型目录 = os.path.join(项目根目录, "模型")


# =========================
# 2. 物理参数
# =========================
方腔边长 = 0.1
顶盖速度 = 10.0
运动黏性系数 = 0.01
流体密度 = 1.0
雷诺数 = 顶盖速度 * 方腔边长 / 运动黏性系数

时间最小值 = 0.0
时间最大值 = 1.0
最终绘图时刻 = 1.0
误差统计时刻 = [0.0, 0.25, 0.50, 0.75, 1.0]


# =========================
# 3. 采样参数
# =========================
随机种子 = 2026
每个时间步监督点数量 = 50
每类边界点数量 = 200
方程残差点数量 = 500

# 小批量训练规模。残差点需要二阶导数，批量不宜过大。
监督点批量 = 4096
边界点批量 = 1024
残差点批量 = 2048


# =========================
# 4. 网络与训练参数
# =========================
网络隐藏层结构 = [20, 20, 20, 20, 20]
激活函数 = "tanh"
学习率 = 1.0e-3
训练轮数 = 20000
打印间隔 = 100

数据损失权重 = 1.0
边界损失权重 = 1.0
动量损失权重 = 0.1
连续性损失权重 = 0.1

# 用于把物理残差变成无量纲量，防止损失量级差异过大。
动量残差尺度 = 顶盖速度 * 顶盖速度 / 方腔边长
连续性残差尺度 = 顶盖速度 / 方腔边长


# =========================
# 5. 输出设置
# =========================
插值网格数量 = 160
图片分辨率 = 300


def 创建输出目录():
    """创建项目运行需要的输出目录。"""
    for 路径 in [数据目录, 图片目录, 表格目录, 模型目录]:
        os.makedirs(路径, exist_ok=True)


def 设置随机种子():
    """设置 numpy 随机种子，保证采样结果可复现。"""
    np.random.seed(随机种子)
